export function getLocationBySlug(slug: string): LocationData | undefined {
  return floridaLocations.find(location => location.slug === slug)
}

export function getAllLocationSlugs(): string[] {
  return floridaLocations.map(location => location.slug)
}

export function getLocationsByCounty(county: string): LocationData[] {
  return floridaLocations.filter(location => location.county === county)
}

export function searchLocations(query: string): LocationData[] {
  const lowerQuery = query.toLowerCase()
  return floridaLocations.filter(location => 
    location.city.toLowerCase().includes(lowerQuery) ||
    location.county.toLowerCase().includes(lowerQuery) ||
    location.description.toLowerCase().includes(lowerQuery)
  )
}
